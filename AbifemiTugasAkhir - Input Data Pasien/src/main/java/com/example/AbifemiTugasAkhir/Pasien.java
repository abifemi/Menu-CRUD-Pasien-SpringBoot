package com.example.AbifemiTugasAkhir;

public class Pasien {
    private String nama;
    private String alamat;
    private String penyakit; 
    private Integer umur;


    public String getNama() { return nama; }
    public void setNama(String nama) { this.nama = nama; }

    public String getAlamat() { return alamat; }
    public void setAlamat(String alamat) { this.alamat = alamat; }

    public String getPenyakit() { return penyakit; }
    public void setPenyakit(String penyakit) { this.penyakit = penyakit; }

    public Integer getUmur() { return umur; }
    public void setUmur(Integer umur) { this.umur = umur; }
}
